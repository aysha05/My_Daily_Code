class Switch {
    public static void main(String[] args) {

        switch (2) {
            case 1: {
                System.out.println("from case 1");
                break;
            }
            case 2: {
                System.out.println("From case 2");
                break;
            }
            case 3: {
                System.out.println("From case 3");
                break;
            }
            default: {
                System.out.println("From default");
            }

        }
    }
}